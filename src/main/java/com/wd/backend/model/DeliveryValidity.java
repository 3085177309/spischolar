package com.wd.backend.model;

public class DeliveryValidity {
	
	private int id;
	
	private String email;
	
	private int count;
	
	private String orgName;
	
	private String orgFlag;
	
	private String endTime;
	
	private String time;
	
	private int procesorId;
	
	private String procesorName;
	
	private String isRegister;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}


	public String getOrgFlag() {
		return orgFlag;
	}

	public void setOrgFlag(String orgFlag) {
		this.orgFlag = orgFlag;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public int getProcesorId() {
		return procesorId;
	}

	public void setProcesorId(int procesorId) {
		this.procesorId = procesorId;
	}

	public String getProcesorName() {
		return procesorName;
	}

	public void setProcesorName(String procesorName) {
		this.procesorName = procesorName;
	}

	public String getIsRegister() {
		return isRegister;
	}

	public void setIsRegister(String isRegister) {
		this.isRegister = isRegister;
	}
	
	

}
